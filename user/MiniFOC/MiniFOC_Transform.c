/* ========== 坐标变换实现 ========== */

/**
 * @brief  Clarke 正变换（3相 → αβ 轴）
 * @note   对齐 SguanFOC v3.1.0 Sguan_math.c:136
 */
void clarke(float *i_alpha, float *i_beta, float i_a, float i_b)
{
    *i_alpha = i_a;
    *i_beta = (i_a + 2.0f * i_b) * INV_SQRT3_F;
}

/**
 * @brief  Park 正变换（αβ 轴 → dq 轴）
 * @note   对齐 SguanFOC v3.1.0 Sguan_math.c:145
 */
void park(float *i_d, float *i_q, float i_alpha, float i_beta, float sine, float cosine)
{
    *i_d = i_alpha * cosine + i_beta * sine;
    *i_q = i_beta * cosine - i_alpha * sine;
}

/**
 * @brief  Park 逆变换（dq 轴 → αβ 轴）
 * @note   对齐 SguanFOC v3.1.0 Sguan_math.c:156
 */
void ipark(float *u_alpha, float *u_beta, float u_d, float u_q, float sine, float cosine)
{
    *u_alpha = u_d * cosine - u_q * sine;
    *u_beta = u_q * cosine + u_d * sine;
}

/**
 * @brief  Clarke 逆变换（αβ 轴 → 3相）
 * @note   对齐 SguanFOC 逆 Clark 公式
 */
void iclarke(float *u_a, float *u_b, float *u_c, float u_alpha, float u_beta)
{
    *u_a = u_alpha;
    *u_b = -0.5f * u_alpha + SQRT3_2_F * u_beta;
    *u_c = -0.5f * u_alpha - SQRT3_2_F * u_beta;
}

/**
 * @brief  快速计算 sin 和 cos（查表法）
 * @note   对齐 SguanFOC v3.1.0 Sguan_math.c:275
 *         512 点查找表，精度 ~0.012°，比 sinf/cosf 快 5-10 倍
 */
void fast_sin_cos(float x, float *sin_x, float *cos_x)
{
    /* 归一化到 [0, 2π) */
    while (x >= TWO_PI_F) x -= TWO_PI_F;
    while (x < 0.0f) x += TWO_PI_F;

    /* 计算查找表索引 */
    float idx_f = x * (SIN_TAB_SIZE / TWO_PI_F);  /* 512 / 2π ≈ 81.487 */
    int idx = (int)idx_f;

    /* 边界保护 */
    if (idx >= SIN_TAB_SIZE) idx = SIN_TAB_SIZE - 1;
    if (idx < 0) idx = 0;

    *sin_x = sin_tab[idx];
    *cos_x = sin_tab[(idx + (SIN_TAB_SIZE / 4)) % SIN_TAB_SIZE];  /* cos = sin(x + π/2) */
}

/**
 * @brief  快速正弦函数（查表法，仅返回 sin）
 * @note   对齐 SguanFOC v3.1.0 Sguan_math.c:257
 */
float fast_sin(float theta)
{
    /* 归一化到 [0, 2π) */
    while (theta >= TWO_PI_F) theta -= TWO_PI_F;
    while (theta < 0.0f) theta += TWO_PI_F;

    /* 计算查找表索引 */
    float idx_f = theta * (SIN_TAB_SIZE / TWO_PI_F);
    int idx = (int)idx_f;

    /* 边界保护 */
    if (idx >= SIN_TAB_SIZE) idx = SIN_TAB_SIZE - 1;
    if (idx < 0) idx = 0;

    return sin_tab[idx];
}
